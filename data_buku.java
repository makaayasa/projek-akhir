    private void tblbukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbukuMouseClicked
        int row = tblbuku.getSelectedRow();
        String a= tblbuku.getValueAt(row, 0).toString();
        String b= tblbuku.getValueAt(row, 1).toString();
        String c= tblbuku.getValueAt(row, 2).toString();
        String d= tblbuku.getValueAt(row, 3).toString();
        String e= tblbuku.getValueAt(row, 4).toString();
        String f= tblbuku.getValueAt(row, 5).toString();
        String g= tblbuku.getValueAt(row, 6).toString();
       
        
        txt_kode.setText(a);
        txt_judul.setText(b);
        if("Ilmiah".equals(c)){
            rilmiah.setSelected(true);
        }
        else{
            rfiksi.setSelected(true);
        }
        txt_tahun.setText(d);
        txt_penerbit.setText(e);
        txt_pengarang.setText(f);
        txt_jumlah.setText(g);
    }//GEN-LAST:event_tblbukuMouseClicked

