    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
    String Kategori = null;
    if(rilmiah.isSelected()){
        Kategori = "Ilmiah";
    }else if(rfiksi.isSelected()){
        Kategori ="Fiksi";
    }
    String sql = "Insert into pendataan values(?,?,?,?,?,?,?)";
    try{
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1,txt_kode.getText());
        stat.setString(2,txt_judul.getText());
        stat.setString(3,Kategori);
        stat.setString(4,txt_tahun.getText());
        stat.setString(5,txt_penerbit.getText());
        stat.setString(6,txt_pengarang.getText());
        stat.setString(7,txt_jumlah.getText());
        
        stat.executeUpdate();
        JOptionPane.showMessageDialog(null,"Data tersimpan");
        kosong();
        txt_kode.requestFocus();
    }
    catch (SQLException e){
        JOptionPane.showMessageDialog(null,"Data belum terinput semua /diupdate");
   
       
    }
        datatable();
    }//GEN-LAST:event_btn_simpanActionPerformed

